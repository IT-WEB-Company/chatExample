                                    <div class="card-body">
                                        <ul class="list-unstyled">
                                            <li><i class="icon-record2 mr-1"></i> If you do not want the default message at all (»Drop files to
                                                upload (or click)«), you can put an element inside your dropzone element with the class <code>dz-message</code>
                                                and dropzone will not create the message for you.</li>

                                            <li><i class="icon-record2 mr-1"></i> Dropzone will submit any hidden fields you have in your dropzone
                                                form. So this is an easy way to submit additional data. You can also use the <code>params</code>
                                                option.</li>

                                            <li><i class="icon-record2 mr-1"></i> Dropzone adds data to the <code>file</code> object you can use when
                                                events fire. You can access <code>file.width</code> and <code>file.height</code> if it’s an image, as
                                                well as <code>file.upload</code> which is an object containing: <code>progress</code> (0-100), <code>total</code>
                                                (the total bytes) and <code>bytesSent</code>.</li>

                                            <li><i class="icon-record2 mr-1"></i> If you want to add additional data to the file upload that has to
                                                be specific for each file, you can register for the <code>sending</code> event:
                                                <pre class="language-javascript">
<code class="language-javascript">
    myDropzone.on("sending", function(file, xhr, formData) {
        // Will send the filesize along with the file as POST data.
        formData.append("filesize", file.size);
    });
</code>
                                </pre>
                                            </li>

                                            <li><i class="icon-record2 mr-1"></i> To access the preview html of a file, you can access <code>file.previewElement</code>.
                                                For example:
                                                <pre class="language-javascript">
<code class="language-javascript">
myDropzone.on("addedfile", function(file) {
    file.previewElement.addEventListener("click", function() {
        myDropzone.removeFile(file);
    });
});
</code>
                                </pre>
                                            </li>

                                            <li><i class="icon-record2 mr-1"></i> If you want the whole body to be a Dropzone and display the files
                                                somewhere else you can simply instantiate a Dropzone object for the body, and define the <code>previewsContainer</code>
                                                option. The <code>previewsContainer</code> should have the <code>dropzone-previews</code> or <code>dropzone</code>
                                                class to properly display the file previews.
                                                <pre class="language-javascript">
<code class="language-javascript">
    new Dropzone(document.body, {
        previewsContainer: ".dropzone-previews",
        // You probably don't want the whole body
        // to be clickable to select files
        clickable: false
    });
</code>
                                </pre>
                                            </li>

                                            <li><i class="icon-record2 mr-1"></i> Look at the <a href="https://github.com/enyo/dropzone/wiki">github
                                                    wiki</a> for more examples.</li>
